char	*ft_strchr(const char *s, int c)
{
	char	*s1_cpy;
	
	s1_cpy = (char *)s;
	while (*s1_cpy)
	{
		if (*s1_cpy == c)
		{
			return (s1_cpy);
		}
		s1_cpy++;
	}
	if (c != 0)
	{
		return (0);
	}
	return (s1_cpy);
}
