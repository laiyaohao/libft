char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s)
	{
		if (s[i] == c)
		{
			return (s);
		}
		i++;
		s++;
	}
	if (c != 0)
	{
		return (0);
	}
	return (s);
}
